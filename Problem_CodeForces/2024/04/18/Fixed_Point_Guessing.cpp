int read()
{
    int x = 0;
    int y = 1;
    char ch = getchar();
    while (ch > '9' || ch < '0')
    {
        if (ch == '-')
            y = -1;
        ch = getchar();
    }
    while (ch >= '0' && ch <= '9')
    {
        x = (x << 3) + (x << 1) + (ch ^ 48);
        ch = getchar();
    }
    return x * y;
}

void write(int x)
{
    if (x < 0)
    {
        putchar('-');
        x = -x;
    }
    if (x >= 10)
    {
        write(x / 10);
    }
    putchar(x % 10 + '0');
}

bool check(int l, int r)
{
    printf("? %d %d\n", l, r);
    fflush(stdout);
    int t;
    int cnt = 0;
    for (int i = l; i <= r; i++)
    {
        scanf("%d", &t);
        if (t >= l && t <= r)
            cnt++;
    }
    if (cnt & 1)
        return true;
    return false;
}

void solve()
{
    int n;
    scanf("%d", &n);
    int l = 1;
    int r = n;
    while (l < r)
    {
        int mid = (l + r) >> 1;
        if (check(l, mid))
            r = mid;
        else
            l = mid + 1;
    }
    printf("! %d\n", l);
    fflush(stdout);
}

int main()
{
    // ios::sync_with_stdio(false), std::cin.tie(nullptr), std::cout.tie(nullptr);
    int _ = 1;
    scanf("%d", &_);
    fflush(stdout);
    while (_--)
    {
        solve();
    }
    return 0;
}

